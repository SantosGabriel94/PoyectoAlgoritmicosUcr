 private void mostrarImagenesActuales() {
      for (int i = 0; i < 7; i++) {
       int index = (movimientosCarrusel.getCurrentIndex() + i) % movimientosCarrusel.size();
        String image = movimientosCarrusel.get(index);
            if (image != null) {
                ImageIcon icon = new ImageIcon(getClass().getClassLoader().getResource(image));
                switch (i) {
                    case 0:
                        jLabel4.setIcon(icon);
                        break;
                    case 1:
                        jLabel5.setIcon(icon);
                        break;
                    case 2:
                        jLabel6.setIcon(icon);
                        break;
                    case 3:
                        jLabel7.setIcon(icon);
                        break;
                    case 4:
                        jLabel8.setIcon(icon);
                        break;
                    case 5:
                        jLabel9.setIcon(icon);
                        break;
                    case 6:
                        jLabel10.setIcon(icon);
                        break;
                    default:
                        break;
                }
            }
        }
    }



